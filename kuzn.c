int kuzn_Ffunc(uint8_t *C, int i, uint8_t keys[10][16]) 
{
	uint8_t tmp[16];

	memcpy(tmp, keys[2*i + 3], 16);
	
	memcpy(keys[2*i + 3], keys[2*i + 2], 16);
	
	kuzn_Xfunc(keys[2*i + 2], C);
	Sfunc(keys[2*i + 2]);
	Lfunc(keys[2*i + 2]);
	kuzn_Xfunc(keys[2*i + 2], tmp);

	return 0;
}


int kuzn_efunc(uint8_t *bl, uint8_t keys[10][16])//basik encription function
{
	int i;

	for (i = 0; i < 9; i++){
		
		kuzn_Xfunc(bl, keys[i]);

		Sfunc(bl);

		Lfunc(bl);
	}

	kuzn_Xfunc(bl, keys[9]);
	return 0;
}

int kuzn_dfunc(uint8_t *bl, uint8_t keys[10][16])//basik decription function
{
	int i;

	kuzn_Xfunc(bl, keys[9]);

	for (i = 8; i >= 0; i--){

		Linv(bl);

		Sinv(bl);

		kuzn_Xfunc(bl, keys[i]);
	
	}
	return 0;
}

uint8_t kuzn_bl_gen_en (uint8_t *bl, FILE *in)
{
	uint8_t *blt;
	int i, c, r = 0;

	blt = bl;
	i = 16;

	while (i > 0){

		if (GENERATOR(in, c)){
			c =(r)? 0x00 : 0x01;// to fill bits by 0
			r = 1;//this means that we reach the EOF
		}

		*blt = c;
		blt++;
		i--;
	}

	return r;
}

uint8_t kuzn_bl_gen_de(uint8_t *bl, FILE *in)
{
	uint8_t *blt;
	int i, c;

	blt = bl;
	i = 16;

	while (i > 0){

		if (GENERATOR(in, c))
			return 1;
		*blt = c;
		blt++;
		i--;
	}

	return 0;
}

uint8_t kuzn_sync_gen (uint8_t *sync, FILE *insync)
{
	int i, c;
	uint8_t *tmp;

	tmp = sync;
	i = 0;

	while (i < 32){

		fscanf(insync, "%2x", &c);
		*tmp = c;
		tmp++;
		i++;
	}

	return 0;
}

uint8_t kuzn_keys_gen (uint8_t keys[10][16], FILE *inkey)
{
	uint8_t C[16] = {0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00};

	int i, c, j;
 
	i = 0;



	while (i < 16){

		fscanf(inkey, "%2x", &c);
		keys[0][i] = c;
		i++;
	}

	i = 0;

	while (i < 16){

		fscanf(inkey, "%2x", &c);
		keys[1][i] = c;
		i++;
	}

	for (i = 0; i < 4; i++){

		memcpy(keys[2*i + 2], keys[2*i], 16);
		memcpy(keys[2*i + 3], keys[2*i + 1], 16);

		for (j = 0; j < 8; j++){
			kuzn_Cfunc(8 * i + j + 1, C);
			kuzn_Ffunc(C, i, keys);
			}
	}

	return 0;
}


