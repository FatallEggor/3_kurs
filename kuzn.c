uint8_t lfunc (uint8_t *bl)
{
	int i;
	uint8_t out = 0, mul = 0;
	uint8_t l_table[16] = {148, 32, 133, 16, 194, 192, 1, 251, 1, 192, 194, 16, 133, 32, 148, 1};
	
	for (i = 0; i < 16; i++){
		mul = gf_fast_mult(bl[i], l_table[i]);
		out ^= mul;
		}
	return out;
}

int Rfunc (uint8_t *bl)
{
	uint8_t tmp;

	tmp = lfunc(bl);
 
	memmove (bl + 1, bl, 15);

	bl[0] = tmp;
	
	return 0;
}

