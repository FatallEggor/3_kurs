int kuzn_simple_mode_de(uint8_t keys[10][16], FILE *in, FILE *out)
{
	int lb = 0, j, y = 0, c;
	uint8_t bl[16], bl_next[16];

	lb = kuzn_bl_gen_de(bl_next, in);

	while (!lb){
		memcpy(bl, bl_next, 16);
		lb = kuzn_bl_gen_de(bl_next, in);

		SHOWPRINT(text, bl);

		kuzn_dfunc(bl, keys);

		SHOWPRINT(decrypted, bl);

		if (!lb){
			for (j = 0; j < 16 ; j++){
				c = bl[j];
				fputc(c,out);
			}
		} else {	
			j = 16;
			while (bl[j] != 0x01)
				j--;
			j--;

			while (y <= j){
				c = bl[y];
				fputc(c,out);
				y++;
			}
		}
			
	}
	fclose(out);


	return 0;
}

